# Install a package;
pip install <pkg-name>

# Upgrades pip itself to the latest version;
pip install --upgrade pip

# Searches PyPI for packages matching the provided query;
pip search <query>

# Lists all installed Python packages;
pip list

# Displays information about a specific Python package, such as its version, location, and dependencies;
pip show <package>

# Uninstalls a Python package;
pip uninstall <package>

# Upgrades a specific Python package to the latest version;
pip install --upgrade <package>